/*
* for循环
* 结构 for(初始化语句; 循环条件; 迭代部分){
*           循环体;
*       }
* */
public class ForTest {
    public static void main(String[] args) {
        //例子 输出5个Hello World
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }
    }
}
